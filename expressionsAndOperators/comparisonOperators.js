//Equal To   only checks the value
let equalTo = "3" == 3;
console.log(equalTo);

//Strict Equals To checks the type and value
let strictEqualTo = "3" === 3;
console.log(strictEqualTo);

//Not Equal To
let notEqualTo = 3 != "3";
console.log(notEqualTo);

//Strict Not Equal To
let strictNotEqualTo = 3 !== "3";
console.log(strictNotEqualTo);

//Greater Than
let greaterThan = 4 > 7;
console.log(greaterThan);

//Less Than
let lessThan = 33 < 100;
console.log(lessThan);

//Greater Than or Equal to
let greaterThanOrEqualTo = 2 >= 3;
console.log(greaterThanOrEqualTo);

//Less Than or Equal to
let lessThanOrEqualTo = 2 <= 3;
console.log(lessThanOrEqualTo);

//And
2 + 2 && 3 + 3

//Or
2 || 3